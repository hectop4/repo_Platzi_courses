#se crean ambientes virtuales
#Para crear un ambiente virtual se usa el comando:
#python -m venv nombre_del_ambiente
#Para activar el ambiente virtual se usa el comando:
#source nombre_del_ambiente/bin/activate
#Para desactivar el ambiente virtual se usa el comando:
#deactivate
#Para instalar un paquete en el ambiente virtual se usa el comando:
#pip install nombre_del_paquete
#Se usan para poder tener diferentes versiones de un mismo paquete en diferentes
#ambientes virtuales
#Para ver los paquetes instalados en el ambiente virtual se usa el comando:
#pip freeze
#Para guardar los paquetes instalados en el ambiente virtual se usa el comando:
#pip freeze > requirements.txt
#pip nos permite instalar y compartir programas de terceros
#Permite especificar la version de cada paquete
#Para instalar los paquetes de un archivo requirements.txt se usa el comando:
#pip install -r requirements.txt



